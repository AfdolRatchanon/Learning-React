//ANCHOR splice ลำสมาชิกใน Array แบบช่วง

//NOTE splice (ตำแหน่างที่จะลย,จำนวนที่จะลบ)
//NOTE splice (ตำแหน่างที่จะลย,จำนวนที่จะลบ,สมาชิกที่ต้องการแทรกเข้าไปในตำแหน่งที่จะลบ)
//REVIEW จะลบข้อมูลในตัวแปรด้วย
const data = [10, 20, 30, 40, 50];
data.splice(2, 3, 999);
console.log(data);

//NOTE (ตำแหน่งเริ่มต้น,ตำแหน่งสุดท้าย-1) //จะไม่ลบข้อมูลในตัวแปร
//REVIEW จะดึงข้อมูลมาใช้งานเป็นตามช่วงที่กำหนด
